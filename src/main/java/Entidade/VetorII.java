package Entidade;

public class VetorII {

    private String nome;
    private double preco;

    public VetorII(String nome, double preco) {
        this.nome = nome;
        this.preco = preco;
    }

    public String getNome(String nome) {
        return nome;
    }

    public void setNome() {
        this.nome = nome;
    }

    public double getPreco() {
        return preco;
    }

    public void setPreco() {
        this.preco = preco;
    }
}
